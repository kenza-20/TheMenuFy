const mongoose = require('mongoose');
const bcrypt = require('bcryptjs');

const UserSchema = new mongoose.Schema({
    name: { type: String, required: true },
    email: { type: String, required: true, unique: true },
    password: { type: String, required: true },
    role: { type: String, enum: ['admin', 'user', 'resto'], default: 'user' },
    isBlocked: { type: Boolean, default: false },
    validated: { type: Boolean, default: false }, // Ajouté pour validation admin
    confirmed: { type: Boolean, default: false }, // Ajouté pour confirmation email
    token: { type: String } // Stocke le token après login
});

// Hashage du mot de passe avant enregistrement
UserSchema.pre('save', async function (next) {
    if (this.isModified('password')) {
        this.password = await bcrypt.hash(this.password, 10);
    }
    next();
});

module.exports = mongoose.model('User', UserSchema);
